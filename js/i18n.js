export const supportedLanguages = Object.keys(translations);

export function detectLanguage() {
  const stored = localStorage.getItem("language");
  if (supportedLanguages.includes(stored)) return stored;
  const browser = navigator.language?.slice(0, 2).toLowerCase();
  return supportedLanguages.includes(browser) ? browser : "en";
}

export function t(key, lang = detectLanguage()) {
  return translations[lang]?.[key] || translations.en[key] || key;
}

export function setLanguage(lang) {
  const next = supportedLanguages.includes(lang) ? lang : "en";
  localStorage.setItem("language", next);
  document.documentElement.lang = next;
  applyTranslations(next);
}

export function setupLanguageSelector(selector = "[data-language-select]") {
  const select = document.querySelector(selector);
  const lang = detectLanguage();
  document.documentElement.lang = lang;
  if (select) {
    select.value = lang;
    select.addEventListener("change", () => setLanguage(select.value));
  }
  applyTranslations(lang);
  return lang;
}

export function applyTranslations(lang = detectLanguage()) {
  document.querySelectorAll("[data-i18n]").forEach((element) => {
    element.textContent = t(element.getAttribute("data-i18n"), lang);
  });
  document.querySelectorAll("[data-i18n-placeholder]").forEach((element) => {
    element.setAttribute("placeholder", t(element.getAttribute("data-i18n-placeholder"), lang));
  });
}
